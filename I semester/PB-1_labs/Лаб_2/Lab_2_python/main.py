
x = float(input("x = "))  # вводимо координату по х
y = float(input("y = "))  # вводимо координату по у
if abs(y) >= pow(x, 2) and abs(x) >= pow(y, 2):  # перевіряємо, чи потрапляє точка у задану площину
    print("Так, належить")  # виводимо результат, якщо умова виповнена
else:  # випадок, коли точка не потрапляє у задану площину
    print("Ні, не належить")  # виводимо результат, якщо умова не виконується
